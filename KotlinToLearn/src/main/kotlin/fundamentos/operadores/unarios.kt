package fundamentos.operadores

fun main() {

    var valorUm: Int = 1
    var valorDois: Int = 2

    valorDois++
    valorUm++

    println(valorDois)
    println(valorUm)


    valorDois--
    valorUm--

    println(valorDois)
    println(valorUm)

    println(++valorDois)
    println(++valorUm)
    println(--valorDois)
    println(--valorUm)

}