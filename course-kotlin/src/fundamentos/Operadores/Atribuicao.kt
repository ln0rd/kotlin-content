package fundamentos.Operadores

fun main(args: Array<String>) {

    var a: Int = 7
    var b: Int = 3

    /**
     * Atribuicao aditiva
     **/

    b += a // b = b + a
    println("Adicao $b")

    /**
     * Atribuicao subtrativa
     **/

    b -= 4 // b = b - 4
    println("Subtracao $b")

    /**
     * Atribuicao multiplativa
     **/

    b *= 2 // b = b * 2
    println("Multiplicacao $b")

    /**
     * Atribuicao Divisiva
     **/

    b /= 2 // b = b / 2
    println("Divisao $b")

    /**
     * Atribuicao por modulo
     **/

    b %= 3 // b = b % 2
    println("Modulo $b")



}